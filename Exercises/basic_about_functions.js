
function printSum(num1, num2) {
    console.log(num1 + num2)
}
printSum(2, 3)



function Sum(a, b) {
    return a + b
}
console.log(Sum(2, 5))



//storing a function in a variable
const printSum = function (num1, num2) {
    console.log(num1 + num2)
}
printSum(2, 3)



//arrow function
const soma = (a, b) => {
    return a + b
}
console.log(soma(1, 2))



//implicit return
const subtraction = (a, b) => a - b
console.log(subtraction(2, 1))
